package raidan;

public class radian {
	public static void main(String args[]) {
		
		double r;
		double radian;
		double radian2;
		
		r=45;
		radian= 3.14*(r*r);
		radian2= 2*3.14*r;
		System.out.println("원의 면적 "+ radian +"이다cm 로계산함");
		System.out.println("원의 둘래 "+ radian2 +"이다cm 로계산함");
	}
}
